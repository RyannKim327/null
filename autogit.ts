let str: string = "123";
let num: number = parseInt(str, 10); // The second argument (10) is the radix (base)
console.log(num); // Output: 123
let str: string = "123";
let num: number = Number(str);
console.log(num); // Output: 123
let str: string = "123";
let num: number = +str; // Unary plus converts string to number
console.log(num); // Output: 123
