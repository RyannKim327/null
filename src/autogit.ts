function isValidEmail(email: string): boolean {
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(email);
}

// Example usage:
console.log(isValidEmail("example@test.com")); // true
console.log(isValidEmail("bad-email@"));       // false
