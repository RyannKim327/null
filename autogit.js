// Taking user input using prompt
let userInput1 = prompt("Enter a number: ");
let userInput2 = prompt("Enter another number: ");

// Converting user input to numbers
let number1 = parseFloat(userInput1);
let number2 = parseFloat(userInput2);

// Performing a basic operation (addition in this case)
let result = number1 + number2;

// Displaying the result
console.log("The sum of the two numbers is: " + result);
