function bubbleSort(arr) {
  var len = arr.length;
  for (var i = 0; i < len; i++) {
    for (var j = 0; j < len - 1 - i; j++) {
      if (arr[j] > arr[j + 1]) {
        var temp = arr[j];
        arr[j] = arr[j + 1];
        arr[j + 1] = temp;
      }
    }
  }
  return arr;
}

// Test the sort algorithm
var array = [4, 2, 7, 1, 3];
console.log("Before sorting: ", array);
console.log("After sorting: ", bubbleSort(array));
