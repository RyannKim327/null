let str = "   Hello,    World!   ";

// Using the replace() method with a regular expression pattern
let result = str.replace(/\s/g, "");

console.log(result); // Output: "Hello,World!"
let str = "   Hello,    World!   ";

// Using the trim() method to remove leading and trailing whitespace
let result = str.trim();

