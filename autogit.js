let str = "  Hello,   World!  ";
let trimmedString = str.replace(/\s/g, "");
console.log(trimmedString); // Output: "Hello,World!"
let str = "  Hello,   World!  ";
let trimmedString = str.split(" ").join("");
console.log(trimmedString); // Output: "Hello,World!"
let str = "  Hello,   World!  ";
let trimmedString = str.trim();
console.log(trimmedString); // Output: "Hello,   World!"
